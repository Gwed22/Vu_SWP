    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("product-add.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if (request.getParameter("btnAdd") != null) {
            String name = request.getParameter("txtName");
            float price = Float.parseFloat(request.getParameter("txtPrice"));
            String img = request.getParameter("txtImg");
            int quantity = Integer.parseInt(request.getParameter("txtQuantity"));
            int category = Integer.parseInt(request.getParameter("Category"));
            String desc = request.getParameter("txtDesc");

            ProductDAO dao = new ProductDAO();
            ResultSet rs = dao.getAll();
            int size = 0;
            try {
                while (rs.next()) {
                    size++;
                }
            } catch (SQLException ex) {
                Logger.getLogger(ProductOrderController.class.getName()).log(Level.SEVERE, null, ex);
            }
            Products pro = new Products(size + 1, name, price, desc, img, category, quantity);

            int count = dao.addNew(pro);
            if (count > 0) {
                response.sendRedirect("/productlist");
            } else {
                response.sendRedirect("/addproduct");
            }
        }
    }

